public class GiostraProvata {
    private String nomeGiostra;
    private String orario;

    public GiostraProvata(String nomeGiostra, String orario) {
        this.nomeGiostra = nomeGiostra;
        this.orario = orario;
    }

    public String getNomeGiostra() {
        return nomeGiostra;
    }

    public String getOrario() {
        return orario;
    }
}
